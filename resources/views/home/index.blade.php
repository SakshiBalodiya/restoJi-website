<script>
    $(document).ready(function () {
        $("#contactSubmit").submit(function (e) {
            e.preventDefault(); // Prevent the default form submission
            $("#formMessages").html("");
            var formData = {
                name: $("#name").val(),
                phone: $("#phone").val(),
                email: $("#email").val(),
                message: $("#message").val(),
                _token: "{{ csrf_token() }}" // Ensure CSRF protection
            };

            $.ajax({
                type: "POST",
                url: "{{ route('contact.submit') }}", // Replace with your Laravel route
                data: formData,
                success: function (response) {
                    $("#formMessages").html(
                        '<div class="alert alert-success">Message sent successfully!</div>'
                    );

                    // Clear form after success
                    $("#contactSubmit")[0].reset();

                    // Hide message after 2 seconds
                    setTimeout(function () {
                        $("#formMessages").fadeOut("slow", function () {
                            $(this).html("").show(); // Clear content and show div again
                        });
                    }, 2000);
                },
                error: function (xhr) {
                    var errors = xhr.responseJSON.errors;
                    var errorHtml = '<div class="alert alert-danger"><ul>';
                    
                    $.each(errors, function (key, value) {
                        errorHtml += '<li>' + value + '</li>'; // Show each error
                    });

                    errorHtml += '</ul></div>';
                    $("#formMessages").html(errorHtml); // Display errors in UI

                    // Hide error messages after 2 seconds
                    setTimeout(function () {
                        $("#formMessages").fadeOut("slow", function () {
                            $(this).html("").show();
                        });
                    }, 2000);
                }
            });
        });
    });
</script>
<script>
    $(document).ready(function () {
        // Show or hide the button based on scroll position
        $(window).scroll(function () {
            if ($(this).scrollTop() > 100) {
                $("#scrollToTop").fadeIn();
            } else {
                $("#scrollToTop").fadeOut();
            }
        });

        // Scroll to top when the button is clicked
        $("#scrollToTop").click(function () {
            $("html, body").animate({ scrollTop: 0 }, 800);
        });
    });
</script>
